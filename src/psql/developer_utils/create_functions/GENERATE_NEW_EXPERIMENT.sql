CREATE OR REPLACE FUNCTION GENERATE_NEW_EXPERIMENT(EXPERIMENT_GROUP_ID BIGINT) RETURNS BIGINT
DECLARE NEW_EXPERIMENT_ID BIGINT;
AS
$$
	SELECT GET_GENE_POOL_COMPETITORS(EXPERIMENT_GROUP_ID);
	SELECT NEW_EXPERIMENT_ID = RECOMBINE_AND_MUTATE(EXPERIMENT_GROUP_ID, PARENT_ALPHA, PARENT_BETA);
	RETURN NEW_EXPERIMENT_ID;
$$
LANGUAGE PLPGSQL
